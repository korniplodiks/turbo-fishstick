// API module for FishstickAPI

class API {
    constructor() {
        this.routes = {};
    }
    
    registerRoute(path, handler) {
        this.routes[path] = handler;
    }
    
    handleRequest(method, path, data = {}) {
        if (this.routes[path]) {
            return this.routes[path](data);
        }
        return { error: 'Not found' };
    }
    
    getRoutes() {
        return Object.keys(this.routes);
    }
}

